# 입력
N, M = map(int, input().split())

# 최대 방문 칸 수 계산
if N == 1 or M == 1:
    # 세로 또는 가로가 1이면 시작점만 방문 가능
    print(1)
elif N == 2:
    # 세로가 2일 때, 가능한 이동: (-1, +2), (+1, +2)
    # 최대 방문 칸 수는 M을 2로 나눈 올림 값과 2 중 작은 값
    print(min((M + 1) // 2, 2))
else:
    # N >= 3일 때
    if M <= 3:
        # 가로가 작을 때, 최대 M칸 방문 가능 (최대 4칸 제한)
        print(min(M, 4))
    else:
        # 가로가 충분히 클 때
        # 4가지 이동을 모두 사용하려면 최소 4번 이동 필요
        # 최대 방문 칸 수는 (M-1)//2 + 2
        print(min(M, (M - 1) // 2 + 2))